package basicprograms;

public class Alphabet {

	public static void main(String[] args) {
		char ch='z';
		if(ch == 'a' || ch == 'i' || ch == 'o' || ch == 'u' || ch =='e' ||
				ch == 'A' || ch == 'I' || ch == 'O' || ch == 'U' || ch =='E' ) {
			System.out.println(ch +" is vowel ");
		}else {
			System.out.println(ch+" is consonant");
		}
    }

}
